
public class Program17 {

    public static void main(String[] args) {
        //Pattern programming (number pattern)
        int n = 5;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
